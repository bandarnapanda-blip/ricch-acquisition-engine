def fetch_new_leads_missing_reports(limit: int = 1000):
    """
    Fetch leads from Supabase that don't have report_pdf_path (or pdf missing).
    Returns list of parsed signals dicts.
    """
    if not supabase:
        raise RuntimeError("Supabase not configured; cannot fetch leads.")
    resp = supabase.table("leads").select("*").execute()
    rows = resp.data if hasattr(resp, 'data') else (resp.get("data", []) if isinstance(resp, dict) else [])
    candidates = []
    for r in rows:
        try:
            signals_json = r.get("signals", {})
            if isinstance(signals_json, str):
                signals_dict = json.loads(signals_json)
            else:
                signals_dict = signals_json or {}
            
            raw = signals_dict.get("raw") or {}
            if raw and raw.get("report_pdf_path"):
                continue
            
            if LeadSignals:
                # Ensure we only pass keys that exist in LeadSignals dataclass
                import dataclasses
                field_names = {f.name for f in dataclasses.fields(LeadSignals)}
                filtered_signals = {k: v for k, v in signals_dict.items() if k in field_names}
                lead = LeadSignals(**filtered_signals)
            else:
                lead = signals_dict
            candidates.append(lead)
            if len(candidates) >= limit:
                break
        except Exception as e:
            logger.warning(f"Skipping row parsing error for {r.get('domain')}: {e}")
    return candidates

def generate_reports_incremental(limit: int = 200):
    try:
        leads = fetch_new_leads_missing_reports(limit=limit)
    except Exception as e:
        logger.error(f"Failed to fetch leads: {e}")
        return

    logger.info(f"Found {len(leads)} leads missing reports")
    created = 0
    for lead in leads:
        try:
            # Only create reports for high-value action
            action = recommend_outreach_action(lead)
            if action != "digital_twin_revenue_simulation":
                continue
            save_mini_report(lead, make_pdf=True, push_to_db=True)
            created += 1
        except Exception as e:
            logger.warning(f"Failed to generate report for {getattr(lead,'domain', 'unknown')}: {e}")
    logger.info(f"Created {created} new reports")

# If run directly
if __name__ == "__main__":
    generate_reports_incremental()
