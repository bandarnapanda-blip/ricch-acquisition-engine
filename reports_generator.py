def _safe_filename(prefix: str, domain: str, ext: str = "html") -> str:
    ts = datetime.utcnow().strftime("%Y%m%d%H%M%S")
    safe = "".join(ch if ch.isalnum() or ch in ("-", "_") else "-" for ch in domain)
    return f"{prefix}_{safe}_{ts}.{ext}"

def generate_report_html(signals: "LeadSignals") -> str:
    """Render HTML string for a lead."""
    issues = []
    if getattr(signals, "slow_load_estimate", None) and signals.slow_load_estimate > 4:
        issues.append(f"Slow load ({signals.slow_load_estimate}s)")
    if getattr(signals, "missing_schema", False):
        issues.append("Missing schema.org markup")
    if getattr(signals, "outdated_theme_hint", False):
        issues.append("Outdated design")
    if getattr(signals, "serp_rank", None) and signals.serp_rank > 10:
        issues.append(f"Low search ranking (#{signals.serp_rank})")
    if not issues:
        issues_summary = "Minor technical & UX improvements"
    else:
        issues_summary = "; ".join(issues)

    preview_link = "#"
    if signals.raw and signals.raw.get("digital_twin_path"):
        preview_link = signals.raw.get("digital_twin_path")

    raw_data = {}
    if signals and hasattr(signals, 'raw'):
        raw_data = signals.raw or {}
    
    html = HTML_TEMPLATE.render(
        domain=getattr(signals, 'domain', 'Unknown') if signals else 'Unknown',
        timestamp=datetime.utcnow().strftime("%Y-%m-%d %H:%M:%S UTC"),
        deal_score=getattr(signals, 'deal_score', 0) if signals else 0,
        action=recommend_outreach_action(signals) if signals else 'ignore',
        slow_load_estimate=getattr(signals, "slow_load_estimate", None),
        serp_rank=getattr(signals, "serp_rank", None),
        has_viewport_meta=getattr(signals, "has_viewport_meta", False),
        multiple_locations=getattr(signals, "multiple_locations", 0),
        signals_json=json.dumps(raw_data or asdict_safe(signals), indent=2),
        issues_summary=issues_summary,
        preview_link=preview_link,
    )
    return html

def asdict_safe(signals):
    """Fallback if dataclass conversion is needed."""
    try:
        from dataclasses import asdict
        return asdict(signals)
    except Exception:
        return signals.__dict__

def save_report_html(signals: "LeadSignals") -> str:
    """Save HTML report to REPORT_DIR and return path."""
    filename = _safe_filename("report", signals.domain, "html")
    path = REPORT_DIR / filename
    html = generate_report_html(signals)
    path.write_text(html, encoding="utf-8")
    logger.info(f"Saved HTML report: {path}")
    return str(path)

def generate_pdf_report(signals: "LeadSignals", pdf_path: str) -> bool:
    """
    Create a simple PDF summary using ReportLab.
    Returns True on success.
    """
    try:
        doc = SimpleDocTemplate(pdf_path, pagesize=letter, rightMargin=36, leftMargin=36, topMargin=36, bottomMargin=36)
        styles = getSampleStyleSheet()
        styleH = styles["Heading1"]
        styleN = styles["BodyText"]
        elems = []

        elems.append(Paragraph(f"AI Audit — {signals.domain}", styleH))
        elems.append(Spacer(1, 8))
        elems.append(Paragraph(f"Generated: {datetime.utcnow().strftime('%Y-%m-%d %H:%M:%S UTC')}", styleN))
        elems.append(Spacer(1, 12))

        # Key table
        data = [
            ["Deal Score", str(getattr(signals, 'deal_score', 0))],
            ["Recommended Action", recommend_outreach_action(signals) if signals else 'ignore'],
            ["Load (s)", str(getattr(signals, "slow_load_estimate", "N/A"))],
            ["SERP rank", str(getattr(signals, "serp_rank", "N/A"))],
            ["Mobile responsive", "Yes" if getattr(signals, "has_viewport_meta", False) else "No"],
            ["Multiple locations", str(getattr(signals, "multiple_locations", 0))],
        ]
        table = Table(data, colWidths=[200, 300])
        table.setStyle(TableStyle([
            ("BACKGROUND", (0, 0), (-1, 0), colors.whitesmoke),
            ("GRID", (0, 0), (-1, -1), 0.25, colors.grey),
            ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
            ("FONTNAME", (0, 0), (-1, -1), "Helvetica"),
        ]))
        elems.append(table)
        elems.append(Spacer(1, 12))

        elems.append(Paragraph("Quick Signals & Notes:", styles["Heading3"]))
        raw_json = json.dumps(signals.raw or asdict_safe(signals), indent=2)
        # For PDF readability, split raw_json into paragraphs of reasonable length
        for chunk in split_text(raw_json, 800):
            elems.append(Paragraph(f" {chunk} ", styleN))
            elems.append(Spacer(1, 6))

        doc.build(elems)
        logger.info(f"Generated PDF report: {pdf_path}")
        return True
    except Exception as e:
        logger.warning(f"PDF generation failed for {signals.domain}: {e}")
        return False

def split_text(text: str, size: int = 800):
    """Split long text into pieces for PDF paragraphs."""
    return [text[i:i+size] for i in range(0, len(text), size)]

def save_mini_report(signals: "LeadSignals", make_pdf: bool = True, push_to_db: bool = True) -> dict:
    """
    Create HTML (and optionally PDF) reports for a lead, store paths in signals.raw,
    and optionally push updated signals JSON to Supabase.
    Returns a dict with file paths.
    """
    out = {}
    if signals is None:
        return out

    html_path = save_report_html(signals)
    out["html_path"] = html_path
    
    current_raw = getattr(signals, 'raw', {}) or {}
    current_raw["report_html_path"] = html_path
    current_raw["report_preview_link"] = f"[View Report]({html_path})"
    if hasattr(signals, 'raw'):
        signals.raw = current_raw

    if make_pdf:
        pdf_filename = _safe_filename("report", signals.domain, "pdf")
        pdf_path = str(REPORT_DIR / pdf_filename)
        ok = generate_pdf_report(signals, pdf_path)
        if ok and hasattr(signals, 'raw') and signals.raw is not None:
            out["pdf_path"] = pdf_path
            signals.raw["report_pdf_path"] = pdf_path
            signals.raw["report_pdf_link"] = f"[Download PDF]({pdf_path})"

    # push to supabase signals column (if supabase available)
    if push_to_db and supabase and signals:
        try:
            domain_val = getattr(signals, 'domain', None)
            if domain_val:
                supabase.table("leads").update({"signals": json.dumps(asdict_safe(signals))}).eq("domain", domain_val).execute()
                logger.info(f"Pushed updated signals for {domain_val} to Supabase")
        except Exception as e:
            logger.warning(f"Failed to push report paths to Supabase: {e}")

    return out

