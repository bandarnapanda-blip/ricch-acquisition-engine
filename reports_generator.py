"""
reports_generator.py

Mini-report generator for Antigravity leads.

- Generates HTML summary report per lead (templated via Jinja2)
- Generates a basic PDF summary using ReportLab
- Saves files under ./reports/ (timestamped)
- Updates `signals` JSON in Supabase (adds report paths and links)
- Includes incremental batch runner to process only leads that don't already have a report
"""

